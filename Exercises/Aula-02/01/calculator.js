const sum = (num1, num2) => {
	return num1 + num2;
};

const subtraction = (minuend, subtrahend) => {
	return minuend - subtrahend;
};

const multiplication = (num1, num2) => {
	return num1 * num2;
};

const division = (dividend, divider) => {
	return dividend / divider;
};

module.exports = { sum, subtraction, multiplication, division };
